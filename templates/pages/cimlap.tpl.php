<h1>Bemutatkozás</h1>
<p>
    A Nyíregyházi Állatpark Nonprofit Kft. (Sosto Zoo) missziója, hogy Magyarország keleti,
    kevésbé fejlett régiójában világszínvonalon mutassa be a felnövekvő nemzedéknek a minket
    körülvevő ökológiai környezetet. Megértesse, felhívja a figyelmet és hirdesse, hogy a földön
    minden mindennel összefügg, és minden faj közül az embernek a legnagyobb a felelőssége azért,
    hogy az állat és növényvilág a jövő nemzedékek számára is megmaradjon.
</p>


