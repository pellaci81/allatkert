<p>
    Európa egyik legnagyobb állatállománnyal rendelkező állatparkja különleges hangulatú tölgyerdőben
    látja vendégül az állatbarátokat. Parkunk aktívan részt vesz fajmegmentési programokban,
    természetvédelmi kutatásokban. Oktató tevékenységünkkel gyarapítjuk az állat- és természetvédelem
    iránt felelősséget vállalók táborát. Munkatársaink a hét minden napján 0-24 óráig azon dolgoznak,
    hogy állataink a legideálisabb körülmények közt éljenek és idelátogató vendégeink számára szép
    élmény legyen a Nyíregyházi Állatparkban tett kirándulás.
</p>

